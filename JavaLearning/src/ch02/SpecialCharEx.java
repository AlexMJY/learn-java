package ch02;

public class SpecialCharEx {
    public static void main(String[] args) {
        System.out.println('\''); // '''는 불가능
        System.out.println("abc\t123\b456"); // \t: tab, \b: backspace
        System.out.println('\n'); // new line
        System.out.println("\"Hello\""); // 큰 타옴표 출력
        System.out.println("c:\\");
    }
}
